# Using a while loop to print numbers from 1 to 5
print("Numbers from 1 to 5 using while loop:")
number = 1
while number <= 5:
    print(number)
    number += 1

# Using a for loop to print numbers from 6 to 10
print("Numbers from 6 to 10 using for loop:")
for number in range(6, 11):
    print(number)
